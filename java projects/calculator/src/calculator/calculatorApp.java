    private void jButton29ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton29ActionPerformed
        n2=Double.parseDouble(jTextField1.getText());
        if(opt=="+"){
            res=n1+n2;
            jTextField1.setText(Double.toString(res));
        }else if(opt=="-"){
            res=n1-n2;
            jTextField1.setText(Double.toString(res));
        }else if(opt=="*"){
            res=n1*n2;
            jTextField1.setText(Double.toString(res));
        }else if(opt=="/"){
            res=n1/n2;
            jTextField1.setText(Double.toString(res));
        }else if(opt=="%"){
            res=n1%n2;
            jTextField1.setText(Double.toString(res));
        }else if(opt=="x^y"){
            jTextField1.setText(Double.toString(Math.pow(n1,n2)));
        }
    }//GEN-LAST:event_jButton29ActionPerformed

